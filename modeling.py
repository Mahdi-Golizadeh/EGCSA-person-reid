class GATE(nn.Module):
    def __init__(self, channel, alpha= 0.5):
        super(GATE, self).__init__()
        self.eca = ECA(channel, gamma= 3, beta= 1)
        self.ema = EMA(channel, factor= 32)
        self.elu1 = nn.ELU()
        self.elu2 = nn.ELU()
        self.relu = nn.ReLU()
    def forward(self, x):
        return self.relu(self.elu1(self.ema(x)) * self.eca(x) + self.elu2(self.eca(x)) * self.ema(x))
    

class BN2d(nn.Module):
    def __init__(self, planes):
        super(BN2d, self).__init__()
        self.bottleneck2 = nn.BatchNorm2d(planes)
        self.bottleneck2.bias.requires_grad_(False)  # no shift
        self.bottleneck2.apply(weights_init_kaiming)

    def forward(self, x):
        return self.bottleneck2(x)


